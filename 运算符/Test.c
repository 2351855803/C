#include<stdio.h>

int main(){

	//求一个数的二进制有几个零
	//思路：通过与1按位&，如果为1，则二进制上此位原本也为1
	int num;
	//计数
	int count = 0;
	scanf_s("%d", &num);

	//int 占用四个字节，总共32个比特位
	for (int i = 0; i < 32; i++){
		// 5  ---------------> 00000000000000000000000000000101
		//                     00000000000000000000000000000001
		// 通过每次把5的二进制右移一位，与1的二进制判断，除了最右边一位，其他都为0，则记到1++
		if (1 == ((num >> i) & 1)){
			count++;
		}
	}

	printf("%d\n", count);


	return 0;
}